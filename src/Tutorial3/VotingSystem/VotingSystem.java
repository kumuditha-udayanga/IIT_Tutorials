package Tutorial3.VotingSystem;

public class VotingSystem {
  int labourPartyVotes = 0;
  int conservativePartyVotes = 0;

  public int getLabourPartyVotes() {
    return labourPartyVotes;
  }

  public void setLabourPartyVotes(int labourPartyVotes) {
    this.labourPartyVotes = labourPartyVotes;
  }

  public int getConservativePartyVotes() {
    return conservativePartyVotes;
  }

  public void setConservativePartyVotes(int conservativePartyVotes) {
    this.conservativePartyVotes = conservativePartyVotes;
  }
}
